package in.handwritten.android.objects;

public class HeadingIndexObject {
    private float xAxis;
    private float yAxis;

    public HeadingIndexObject(float xAxis,float yAxis){
        this.xAxis = xAxis;
        this.yAxis = yAxis;
    }

    public float getYAxis() {
        return yAxis;
    }

    public float getXAxis() {
        return xAxis;
    }
}
